# Two coins are thrown twice. With what probability
# (a) two heads
# (b) at least one tail
# are obtained?

# (a) Probability of two heads
prob_a = 1 / 4

# (b) Probability of at least one tail
prob_b = 3 / 4

# Results
print(f"(a) Probability of two heads: {prob_a:.2f}")
print(f"(b) Probability of at least one tail: {prob_b:.2f}")